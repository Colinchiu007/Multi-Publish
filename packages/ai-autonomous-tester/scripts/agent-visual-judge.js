/**
 * Agent 视觉判断报告生成器
 *
 * 用途: 扫描 screenshot 和 diff 目录，生成 Markdown 报告供 Agent 读取并判断
 *
 * 环境变量:
 *   AGENT_SCREENSHOT_DIR  截图目录 (默认 screenshots)
 *   AGENT_BASELINE_DIR   基线目录 (默认 base-screenshots)
 *   AGENT_DIFF_DIR       差异图目录 (默认 reports/pixel-diff)
 *   AGENT_REPORT_DIR     报告输出目录 (默认 reports)
 *
 * Agent 使用方式:
 *   1. 运行像素对比测试
 *   2. 运行本脚本: node scripts/agent-visual-judge.js
 *   3. Agent 读取 reports/judge-report.md
 *   4. Agent 用 view_image 工具看截图，自己判断
 */

const fs = require("fs");
const path = require("path");

const SCREENSHOT_DIR = process.env.AGENT_SCREENSHOT_DIR || "screenshots";
const BASELINE_DIR = process.env.AGENT_BASELINE_DIR || "base-screenshots";
const DIFF_DIR = process.env.AGENT_DIFF_DIR || "reports/pixel-diff";
const REPORT_DIR = process.env.AGENT_REPORT_DIR || "reports";
const OUTPUT_MD = path.join(REPORT_DIR, "judge-report.md");
const OUTPUT_JSON = path.join(REPORT_DIR, "agent-judge-results.json");

const C = { reset: "\x1b[0m", red: "\x1b[31m", green: "\x1b[32m", yellow: "\x1b[33m", blue: "\x1b[34m" };

function log(c, tag, msg) { console.log(C[c] + "[" + tag + "]" + C.reset + " " + msg); }
function toFileUrl(absPath) { return "file:///" + absPath.replace(/\\/g, "/"); }

// 扫描差异图
function scanDiffImages() {
  if (!fs.existsSync(DIFF_DIR)) return [];
  return fs.readdirSync(DIFF_DIR).filter(f => f.endsWith(".png")).map(f => path.join(DIFF_DIR, f)).sort((a, b) => fs.statSync(b).mtime - fs.statSync(a).mtime);
}

// 从差异图文件名提取测试名
function extractTestName(diffPath) {
  return path.basename(diffPath, ".png").replace(/-\d+$/, "");
}

// 扫描截图
function scanScreenshots() {
  if (!fs.existsSync(SCREENSHOT_DIR)) return [];
  return fs.readdirSync(SCREENSHOT_DIR).filter(f => f.endsWith(".png")).map(f => path.join(SCREENSHOT_DIR, f)).sort((a, b) => fs.statSync(b).mtime - fs.statSync(a).mtime);
}

// 主流程
function main() {
  console.log("\n Agent visual judge report generator\n");
  [SCREENSHOT_DIR, BASELINE_DIR, DIFF_DIR, REPORT_DIR].forEach(d => fs.mkdirSync(d, { recursive: true }));

  const diffImages = scanDiffImages();
  log("blue", "INFO", diffImages.length + " diff images found");

  const screenshots = scanScreenshots();
  log("blue", "INFO", screenshots.length + " screenshots found");

  if (screenshots.length === 0) {
    log("yellow", "WARN", "No screenshots found. Run pixel tests first.");
    process.exit(0);
  }

  // 建立 testName -> info 映射
  const testMap = new Map();
  screenshots.forEach(sc => {
    const testName = path.basename(sc, ".png").replace(/-current$/, "");
    const baselinePath = path.join(BASELINE_DIR, testName + ".png");
    const matchedDiff = diffImages.find(d => extractTestName(d) === testName);
    testMap.set(testName, {
      testName,
      screenshotPath: sc,
      baselinePath: fs.existsSync(baselinePath) ? baselinePath : null,
      hasDiff: !!matchedDiff,
      diffPath: matchedDiff || null,
      route: "/"
    });
  });

  // 只处理有差异图的测试
  const failedTests = Array.from(testMap.values()).filter(r => r.hasDiff);

  if (failedTests.length === 0) {
    log("green", "RESULT", "All pixel comparisons passed.");
    process.exit(0);
  }

  log("yellow", "FOUND", failedTests.length + " tests with pixel diff. Generating report...");

  // 生成 Markdown 报告
  const now = new Date().toLocaleString("zh-CN", { timeZone: "Asia/Shanghai" });
  let md = "# Agent Visual Judge Report\n\n";
  md += "**Time:** " + now + "\n";
  md += "**Failed tests:** " + failedTests.length + "\n\n";
  md += "Please use **view_image** tool to examine the screenshots below and judge whether each failure is a real UI bug.\n\n---\n\n";

  failedTests.forEach((r, i) => {
    md += "### " + (i+1) + ". " + r.testName + "\n\n";
    md += "- Route: `" + r.route + "`\n";
    md += "- Screenshot: `" + r.screenshotPath + "`\n";
    md += "- Baseline: `" + (r.baselinePath || "N/A") + "`\n";
    md += "- Diff: `" + r.diffPath + "`\n\n";
    md += "**Comparison table:**\n\n";
    md += "| Type | Path |\n";
    md += "|------|------|\n";
    if (r.baselinePath) md += "| Baseline | " + toFileUrl(r.baselinePath) + " |\n";
    md += "| Current | " + toFileUrl(r.screenshotPath) + " |\n";
    if (r.diffPath) md += "| Diff | " + toFileUrl(r.diffPath) + " |\n";
    md += "\n**Agent judgment:**\n\n";
    md += "- [ ] **PASS**: Visual is correct, diff is expected UI change\n";
    md += "- [ ] **FAIL**: Real UI bug (layout broken, wrong text, missing element)\n";
    md += "- [ ] **NEED INFO**: Uncertain, need more context\n\n";
    md += "**Agent notes:** _\n\n---\n\n";
  });

  md += "---\n*Generated by @visual-test-runner/core agent-visual-judge.js*\n";

  // 生成 JSON
  const jsonReport = {
    generatedAt: new Date().toISOString(),
    failedCount: failedTests.length,
    tests: failedTests.map(r => ({
      testName: r.testName,
      screenshotPath: r.screenshotPath,
      baselinePath: r.baselinePath,
      diffPath: r.diffPath,
      route: r.route
    })),
    reportPath: OUTPUT_MD,
    reportFileUrl: toFileUrl(OUTPUT_MD)
  };

  fs.writeFileSync(OUTPUT_MD, md, "utf8");
  fs.writeFileSync(OUTPUT_JSON, JSON.stringify(jsonReport, null, 2), "utf8");

  log("green", "DONE", "Report: " + OUTPUT_MD);
  log("blue", "URL", toFileUrl(OUTPUT_MD));
  console.log("\nNext: Read " + path.basename(OUTPUT_MD) + " in your Agent session and use view_image tool.\n");
}

main();